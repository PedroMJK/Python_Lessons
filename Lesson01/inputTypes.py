# Para pegar um valor dentro de uma variável
name = input("Qual é o seu nome?: ")
age = int(input("Quantos anos você tem?: "))
height = float(input("Qual é a sua altura?: "))

print("Olá, " + name);
print("Você tem " +str(age) + " anos de idade.");
print("Você tem " +str(height) + " cm de altura.");
